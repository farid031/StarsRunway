package com.farid.starsrunway.crud.visit;

public class ASM {
    private String asm;
    private String namaasm;
    private String password;
    private String tkuser;

    public String getAsm() {
        return asm;
    }
    public void setAsm(String asm) {
        this.asm = asm;
    }
    public String getNamaAsm() {
        return namaasm;
    }
    public void setNamaAsm(String namaasm) {
        this.namaasm = namaasm;
    }
    public String getPass() {
        return password;
    }
    public void setPass(String password) {
        this.password = password;
    }
    public String getTkUser() {
        return tkuser;
    }
    public void setTkuser(String tkuser) {
        this.tkuser = tkuser;
    }
}
